// Du kannst Werte auf Variablen addieren:  
let score = 0;  
score = score + 10;  
console.log('Ergebnis: ' + score);
//Hiervon gibt es eine verkürzte Version:  
score += 10;  
console.log('Ergebnis: ' + score)
